package profiledb;

public class Category {
    private int id;
    private String title;

    // Get methods
    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    // Set methods
    public void setId(int id) {
        this.id = id;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
